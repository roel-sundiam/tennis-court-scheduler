// Team generation interfaces
interface Team {
  id: string;
  player1: Player;
  player2: Player;
  averageSeed: number;
}

interface Match {
  id: string;
  teamA: Team;
  teamB: Team;
}

interface GeneratedMatches {
  dateId: string;
  algorithm: string;
  teams: Team[];
  matches: Match[];
  reservePlayers: Player[];
  autoGenerated?: boolean;
}

interface ManualPairingState {
  availablePlayers: Player[];
  selectedPlayers: Player[];
  teams: Team[];
  reservePlayers: Player[];
}

